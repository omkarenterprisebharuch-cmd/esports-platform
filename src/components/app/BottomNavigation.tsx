/**
 * Mobile Bottom Navigation Bar
 * 
 * Features:
 * - Fixed bottom positioning with safe area support
 * - Active state indicator for current page
 * - Touch-friendly targets (min 44px)
 * - Smooth transitions
 * - Only visible on mobile (< md breakpoint)
 */
export function BottomNavigation() {
  const pathname = usePathname();

  // Check if current path matches or is a child of nav item path
  const isActive = (href: string) => {
    if (href === "/app") {
      return pathname === "/app";
    }
    return pathname?.startsWith(href);
  };

  return (
    <>
      {/* Spacer to prevent content from being hidden behind nav */}
      <div className="h-20 md:hidden" aria-hidden="true" />
      
      {/* Bottom Navigation */}
      <nav
        className="fixed bottom-0 left-0 right-0 z-50 md:hidden bg-white dark:bg-gray-900 border-t border-gray-200 dark:border-gray-800 shadow-lg"
        style={{ paddingBottom: "env(safe-area-inset-bottom, 0px)" }}
        role="navigation"
        aria-label="Main navigation"
      >
        <div className="flex items-center justify-around px-2">
          {navItems.map((item) => {
            const active = isActive(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`
                  relative flex flex-col items-center justify-center
                  min-w-[64px] min-h-[56px] py-2 px-3
                  transition-all duration-200 ease-out
                  ${active 
                    ? "text-orange-600 dark:text-orange-400" 
                    : "text-gray-500 dark:text-gray-400 hover:text-gray-700 dark:hover:text-gray-300"
                  }
                `}
                aria-current={active ? "page" : undefined}
              >
                {/* Active indicator pill */}
                {active && (
                  <span 
                    className="absolute top-1 left-1/2 -translate-x-1/2 w-12 h-8 bg-orange-100 dark:bg-orange-900/30 rounded-full -z-10"
                    aria-hidden="true"
                  />
                )}
                
                {/* Icon */}
                <span className="relative">
                  {active ? item.activeIcon : item.icon}
                </span>
                
                {/* Label */}
                <span className={`
                  text-[10px] mt-1 font-medium truncate max-w-[60px]
                  ${active ? "font-semibold" : ""}
                `}>
                  {item.label}
                </span>
              </Link>
            );
          })}
        </div>
      </nav>
    </>
  );
}

export default BottomNavigation;
